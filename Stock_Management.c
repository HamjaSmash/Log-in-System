void adminRegister() {
    if (adminCount >= MAX_USERS) {
        printf("Sorry, Admin limit reached!\n");
        return;
    }

    printf("\nAdmin Register\nUsername: ");
    scanf("%s", adminList[adminCount].username);
    printf("Password: ");
    scanf("%s", adminList[adminCount].password);

    adminCount++;
    printf("Admin registered successfully!\n");
}

void adminLogin() {
    char username[30], password[30];
    printf("\nAdmin Login\nUsername: ");
    scanf("%s", username);
    printf("Enter Password: ");
    scanf("%s", password);

    for (int i = 0; i < adminCount; i++) {
        if (strcmp(username, adminList[i].username) == 0 &&
            strcmp(password, adminList[i].password) == 0) {
            printf("Admin login successful!\n");
            return;
        }
    }
    printf("Unfortunately, Login failed!\n");
}

void adminMenu() {
    int choice;
    printf("\n------ Admin Menu ------\n1. Register\n2. Login\nChoose option: ");
    scanf("%d", &choice);

    if (choice == 1) adminRegister();
    else if (choice == 2) adminLogin();
    else printf("Invalid option!\n");
}

